// HSL lightness (0-255 scale) approximation from a #rrggbb hex string.
function hexLightness(hex) {
  const r = parseInt(hex.slice(1, 3), 16), g = parseInt(hex.slice(3, 5), 16), b = parseInt(hex.slice(5, 7), 16);
  return (Math.max(r, g, b) + Math.min(r, g, b)) / 2;
}

// Scales a #rrggbb hex color's channels by `factor` (< 1 = darker), keeping
// its hue/relative proportions — same color, just dimmer.
const DARKEN_FACTOR = 0.75;
function darken(hex, factor) {
  const r = parseInt(hex.slice(1, 3), 16), g = parseInt(hex.slice(3, 5), 16), b = parseInt(hex.slice(5, 7), 16);
  const ch = v => Math.max(0, Math.min(255, Math.round(v * factor))).toString(16).padStart(2, '0');
  return `#${ch(r)}${ch(g)}${ch(b)}`;
}

// Darkest stop from an element's own icon gradient — used for the
// name/level/mindscape 3D text effect's shadow layer, so the color always
// matches the icon exactly with no separate color source to keep in sync.
export function getZzzElementColor(elementType) {
  const def = ELEMENT_SVGS[elementType];
  if (!def) return null;
  let dark = def.gradientStops[0];
  for (const s of def.gradientStops) {
    if (hexLightness(s.color) < hexLightness(dark.color)) dark = s;
  }
  return darken(dark.color, DARKEN_FACTOR);
}

